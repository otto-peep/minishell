#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		a;
	char	*b;

	a = 0;
	b = NULL;
	while (s[a] != '\0')
	{
		if (s[a] == c)
			b = (char *)&s[a];
		a++;
	}
	if (s[a] == c)
		return ((char *)&s[a]);
	else
		return (b);
}
